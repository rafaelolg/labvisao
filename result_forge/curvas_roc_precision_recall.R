postscript(file="../tex/roc.eps", paper="special", width=10, height=10, horizontal=FALSE)
plot(1-t0$ESPECIFICIDADE, t0$SENSITIVIDADE, type='l', col='2', xlab='Taxa de falsos positivos', ylab='Taxa de positivos verdadeiros')
